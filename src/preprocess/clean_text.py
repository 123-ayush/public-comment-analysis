import re

def clean_comment(text):
    if not isinstance(text, str):
        return ""
    
    text = text.lower()
    text = re.sub(r"thanks|regards|thank you|sir|madam", " ", text)
    text = re.sub(r"http\S+|www\S+", " ", text)
    text = re.sub(r"[^a-zA-Z0-9\s]", " ", text)
    text = re.sub(r"\s+", " ", text).strip()
    
    return text

